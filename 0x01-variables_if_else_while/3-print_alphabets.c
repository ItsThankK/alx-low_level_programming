#include <stdio.h>
/* betty style doc for function main goes there */
/**
 * main - Prints the alphabets a to z in lowercase 
 *        followed by A to Z in uppercase 
 *
 * Return: Always 0.
*/
int main(void)
{
	char alphabet;

	for (alphabet = 'a'; alphabet <= 'z'; alphabet++)
		putchar(alphabet);
	for (alphabet = 'A'; alphabet <= 'Z'; alphabet++)
		putchar(alphabet);
	putchar('\n');
	return (0);
}
